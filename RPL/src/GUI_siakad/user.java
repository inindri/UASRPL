    public void User_Load() throws SQLException 
    {
        pst = conn.prepareStatement("selesct * from user");
        rs = pst.executeQuery();
        
        ResultSetMetaData rad =  (ResultSetMetaData) rs.getMetaData();
        c = rad.getColumnCount();
        
        d = (DefaultTableModel)jTable1.getModel();
        d.setRowCount(0);
        
        while(rs.next())
        {
            Vector v2 = new Vector();
            
            for(int i=1; i<c; i++)
                    {
                v2.add(rs.getString("id"));
                v2.add(rs.getString("nama"));
                v2.add(rs.getString("username"));
                v2.add(rs.getString("usertype"));
                        
            }
            d.addRow(v2);
        }
    }

    
